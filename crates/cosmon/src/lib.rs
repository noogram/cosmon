//! # Cosmon
//!
//! This crate reserves the `cosmon` name on crates.io. It contains no
//! functionality.
//!
//! Cosmon is a stateless CLI that gives AI coding agents a persistent
//! identity, a typed lifecycle, and crash-recovery — so you can run ten
//! agent sessions in parallel without losing track of who is doing what.
//! It is distributed as a single binary named **`cs`**, not as a library.
//!
//! ## Installing the `cs` binary
//!
//! Prebuilt binaries for Linux and macOS are attached to each GitHub Release:
//! <https://github.com/noogram/cosmon/releases>
//!
//! Or build from source:
//!
//! ```text
//! cargo install --git https://github.com/noogram/cosmon cosmon-cli
//! ```
//!
//! Documentation: <https://docs.noogram.org>
